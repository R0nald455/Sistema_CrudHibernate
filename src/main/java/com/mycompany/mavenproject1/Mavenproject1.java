    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        
        Session misesion =myfactory.openSession();
        try{
        String nombre=txtNombre.getText();
        String apellido=txtApellido.getText();
        String telefono=txtTelefono.getText();
        clientes cli1=new clientes(nombre,apellido,telefono);
        misesion.beginTransaction();
        misesion.save(cli1); 
        misesion.getTransaction().commit();
        System.out.println("registro guaradado");
        misesion.clear();
        }catch(Exception e){
        misesion.clear();
        }
    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        Session misesion =myfactory.openSession();
        try{
        int code=Integer.parseInt(txtCodigo.getText());
        misesion.beginTransaction();
        Object clientes=misesion.get(clientes.class,code);
        misesion.delete(clientes);
        misesion.getTransaction().commit();
        System.out.println("registro eliminado");
        misesion.clear();
        }catch(Exception e){
        misesion.clear();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

