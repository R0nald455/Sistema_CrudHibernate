    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       
        clientes cliente=null;
        Session misesion =myfactory.openSession();
        try{
        int code=Integer.parseInt(txtCodigo.getText());
        misesion.beginTransaction();
        cliente=(clientes) misesion.get(clientes.class,code);
        txtCodigo.setText(Integer.toString(cliente.getId()));
        txtNombre.setText(cliente.getNombre());
        txtApellido.setText(cliente.getApellido());
        txtTelefono.setText(cliente.getTelefono());
        misesion.clear();
        }catch(Exception e){
        misesion.clear();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Session misesion =myfactory.openSession();
        try{
        int code=Integer.parseInt(txtCodigo.getText());
        String nombre=txtNombre.getText();
        String apellido=txtApellido.getText();
        String telefono=txtTelefono.getText();
        clientes cli1=new clientes(nombre,apellido,telefono);
        cli1.setId(code);
        misesion.beginTransaction();
        misesion.update(cli1); 
        misesion.getTransaction().commit();
        System.out.println("registro guaradado");
        misesion.clear();
        }catch(Exception e){
        misesion.clear();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    
    
